    private void btnSendToStampIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToStampIdActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTesting.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }
        
        WorkRequest request = (WorkRequest)tblTesting.getValueAt(selectedRow, 0);
        if (!request.getAssigned()) {
            JOptionPane.showMessageDialog(this, "Work request not assigned by you, so assign request first.");
            return;
        }
        request.setAssigned(false);
        Organization org = null;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (o instanceof StampIdOrganization){
                org = o;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
        }

        populateTable();
    }//GEN-LAST:event_btnSendToStampIdActionPerformed

    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblTesting.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }

        WorkRequest request = (WorkRequest)tblTesting.getValueAt(selectedRow, 0);
        if (!request.getAssigned()) {
            request.setReceiver(account);
            request.setStatus("Pending");
            request.setAssigned(true);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(this, "This work request is already assigned.");
            return;
        }
    }//GEN-LAST:event_btnAssignToMeActionPerformed


