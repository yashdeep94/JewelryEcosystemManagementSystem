    private void btnSendToShopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToShopActionPerformed
        String goldWeight = txtGoldWeight.getText();
        if (goldWeight.isBlank()) {
            JOptionPane.showMessageDialog(this, "Gold weight cannot be blank.");
            return;
        }
        double price;
        try {
            price = Double.parseDouble(txtPrice.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Price should be a number.");
            return;
        }
        if (price <= 0) {
            JOptionPane.showMessageDialog(this, "Price should be grater than zero.");
            return;
        }
        String message = "Gold available for sell : weight - " + goldWeight + " price - " + String.valueOf(price);
        WorkRequest request = new WorkRequest();
        request.setMessage(message);
        request.setSender(account);
        request.setStatus("Sent");
        Enterprise ent = null;
        for (Enterprise e: system.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof JewelleryShopEnterprise) {
                ent = e;
                break;
            }
        }
        if (ent != null){
            ent.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
        }
        JOptionPane.showMessageDialog(this, "Sent to Shop");
        txtGoldWeight.setText("");
        txtPrice.setText("");
        populateTable();
    }//GEN-LAST:event_btnSendToShopActionPerformed

    private void btnSendToBillingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToBillingActionPerformed
        int selectedRow = tblGoldSelling.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }
        
        WorkRequest request = (WorkRequest) tblGoldSelling.getValueAt(selectedRow, 0);
        request.setAssigned(false);
        Organization org = null;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (o instanceof AccountOrganization){
                org = o;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
        }
        populateTable();
    }//GEN-LAST:event_btnSendToBillingActionPerformed


