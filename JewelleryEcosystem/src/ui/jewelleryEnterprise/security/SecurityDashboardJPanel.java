    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblSecurity.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }

        WorkRequest request = (WorkRequest)tblSecurity.getValueAt(selectedRow, 0);
        if (!request.getAssigned()) {
            request.setReceiver(account);
            request.setStatus("Pending");
            request.setAssigned(true);
            populateTable();
        } else {
            JOptionPane.showMessageDialog(this, "This work request is already assigned.");
            return;
        }
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnVerifyBillAndProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifyBillAndProductActionPerformed
        int selectedRow = tblSecurity.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }
        WorkRequest request = (WorkRequest) tblSecurity.getValueAt(selectedRow, 0);
        if (!request.getAssigned()) {
            JOptionPane.showMessageDialog(this, "Work request not assigned by you, so assign request first.");
            return;
        }
        request.setStatus("Done");
        request.setResolveDate(new Date());
        populateTable();
    }//GEN-LAST:event_btnVerifyBillAndProductActionPerformed


