    private void btnSendToManActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToManActionPerformed
        int selectedRow = tblCustomer.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select customer first.");
            return;
        }
        CustomerProfile customer = (CustomerProfile) tblCustomer.getValueAt(selectedRow, 0);
        String name = txtProductName.getText();
        if (name.isBlank()) {
            JOptionPane.showMessageDialog(this, " Product name cannot be blank.");
            return;
        }
        double weight;
        double charges;
        try {
            weight = Double.parseDouble(txtProductWeight.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Product weight should be a number.");
            return;
        }
        try {
            charges = Double.parseDouble(txtRepairCharges.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Repair Charges should be a number.");
            return;
        }
        if (weight <= 0) {
            JOptionPane.showMessageDialog(this, "Product weight should be grater than zero.");
            return;
        }
        if (charges <= 0) {
            JOptionPane.showMessageDialog(this, "Repair charges should be grater than zero.");
            return;
        }
        // Creating repairing product and its associated order
        Product product = enterprise.getProducts().CreateNewProduct(Product.ProductType.Repair, name, weight, charges);
        this.order = enterprise.getMasterOrderList().CreateNewOrder(customer);
        this.order.getProductList().add(product); 
        String message = "";
        for (Product p: this.order.getProductList()) {
            message += p.getName()+" ";
            message += "Weight: "+ String.valueOf(p.getWeight())+ "\n";
        }
        message += "$" +String.valueOf(this.order.getId());
        WorkRequest request = new WorkRequest();
        request.setMessage(message);
        request.setSender(account);
        request.setStatus("Sent");
        Organization org = null;
        for (Enterprise e: system.getNetworkList().get(0).getEnterpriseDirectory().getEnterpriseList()) {
            for (Organization o : e.getOrganizationDirectory().getOrganizationList()){
                if (o instanceof HandCraftOrganization){
                    org = o;
                    break;
                }
            }
            if (org!=null){
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
        }
        JOptionPane.showMessageDialog(this, "Sent to manufacturing");
        txtSearchCustomer.setText("");
        txtProductName.setText("");
        txtProductWeight.setText("");
        txtRepairCharges.setText("");
        order = null;
        populateTable();
    }//GEN-LAST:event_btnSendToManActionPerformed


