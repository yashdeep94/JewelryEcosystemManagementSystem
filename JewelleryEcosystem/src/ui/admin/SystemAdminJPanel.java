    private void populateCombo() {
        comboBoxNetwork.removeAllItems();
        for (Network network: system.getNetworkList()){
            comboBoxNetwork.addItem(network.toString());
        }
    }
    
    private void populateEnterpriseTable() {
        String selectedNetworkString = (String) comboBoxNetwork.getSelectedItem();
        Network selectedNetwork = system.findNetwork(selectedNetworkString);
        if (selectedNetwork == null){
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : selectedNetwork.getEnterpriseDirectory().getEnterpriseList()) {
            Object row[] = new Object[1];
            row[0] = enterprise;
            model.addRow(row);
        }
    }
    
    private void populateEnterpriseTable(String keyword) {
        String selectedNetworkString = (String) comboBoxNetwork.getSelectedItem();
        Network selectedNetwork = system.findNetwork(selectedNetworkString);
        if (selectedNetwork == null){
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblEnterprise.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : selectedNetwork.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise.getName().contains(keyword)) {
                Object row[] = new Object[1];
                row[0] = enterprise;
                model.addRow(row);
            }
        }
    }

