    private void tblEnterpriseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEnterpriseMouseClicked
        // TODO add your handling code here:
        int selectedRow = tblEnterprise.getSelectedRow();

        if (selectedRow < 0){
            return;
        }
        Enterprise enterprise = (Enterprise) tblEnterprise.getValueAt(selectedRow, 0);
        DefaultTableModel model = (DefaultTableModel) tblLocalAdmins.getModel();
        model.setRowCount(0);
        for (UserAccount userAccount: enterprise.getUserAccountDirectory().getUserAccounts()) {
            Object row[] = new Object[2];
            row[0] = userAccount;
            row[1] = userAccount.getRole().toString();
            model.addRow(row);
        }
    }//GEN-LAST:event_tblEnterpriseMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            Gold.changeGoldPrice(Double.parseDouble(txtGoldPrice.getText()));
            JOptionPane.showMessageDialog(this, "Gold Price Changed Successfully.");
            txtGoldPrice.setText("");
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Enter a valid number.");
            return;
        }
    }//GEN-LAST:event_btnSaveActionPerformed


