    private void populateOrderTable(){
        
        DefaultTableModel model = (DefaultTableModel) tblOrders.getModel();
        model.setRowCount(0);
        for (Order order: enterprise.getMasterOrderList().getOrders()) {
            Object row[] = new Object[2];
            row[0] = order;
            row[1] = order.getTotalPrice();
            model.addRow(row);
        }
    }
    private void populateCombo() {
        comboBoxOrganization.removeAllItems();
        for (Organization organization: enterprise.getOrganizationDirectory().getOrganizationList()){
            comboBoxOrganization.addItem(organization.toString());
        }
    }
    
    public void populateProductTable() {
        DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
        model.setRowCount(0);
        for (Product product: enterprise.getProducts().getProducts()) {
            Object row[] = new Object[2];
            row[0] = product;
            row[1] = product.getPrice();
            model.addRow(row);
        }
    }
    
    private void populateTable(){
        
        DefaultTableModel model = (DefaultTableModel) tblAdmin.getModel();
        model.setRowCount(0);

        for (WorkRequest wr : enterprise.getWorkQueue().getWorkRequestList()) {
            Object row[] = new Object[6];
            
            row[0] = wr;
            row[1] = wr.getReceiver();
            row[2] = wr.getMessage();
            row[3] = wr.getStatus();
            row[4] = wr.getRequestDate();
            row[5] = wr.getResolveDate();
            
            model.addRow(row);
        
        }
    }

