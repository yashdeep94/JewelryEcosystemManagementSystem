    private void btnSendToBankActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToBankActionPerformed
        int selectedRow = tblAdmin.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select work request first.");
            return;
        }

        WorkRequest request = (WorkRequest) tblAdmin.getValueAt(selectedRow, 0);
        request.setAssigned(false);
        request.setMessage("Forward to billing department $1");
        request.setReceiver(account);
        request.setStatus("Pending");
        account.getWorkQueue().getWorkRequestList().add(request);
        populateTable();
    }//GEN-LAST:event_btnSendToBankActionPerformed
    
    private void populateEmployeeTable() {
        String selectedOrganizationString = (String) comboBoxOrganization.getSelectedItem();
        Organization selectedOrganization = enterprise.getOrganizationDirectory().findOrganization(selectedOrganizationString);
        if (selectedOrganization == null){
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        model.setRowCount(0);
        for (EmployeeProfile employee : selectedOrganization.getEmployeeDirectory().getEmployeeList()) {
            Object row[] = new Object[2];
            row[0] = employee;
            row[1] = employee.getName();
            model.addRow(row);
        }
    }
    
    private void populateEmployeeTable(String keyword) {
        String selectedOrganizationString = (String) comboBoxOrganization.getSelectedItem();
        Organization selectedOrganization = enterprise.getOrganizationDirectory().findOrganization(selectedOrganizationString);
        if (selectedOrganization == null){
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblEmployee.getModel();
        model.setRowCount(0);
        for (EmployeeProfile employee : selectedOrganization.getEmployeeDirectory().getEmployeeList()) {
            if (employee.getName().contains(keyword) || String.valueOf(employee.getId()).contains(keyword)) {
                Object row[] = new Object[2];
                row[0] = employee;
                row[1] = employee.getName();
                model.addRow(row);
            }
        }
    }

