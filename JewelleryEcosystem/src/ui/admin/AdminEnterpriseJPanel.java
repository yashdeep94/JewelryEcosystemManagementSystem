    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProduct.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select product first.");
            return;
        }
        Product product = (Product) tblProduct.getValueAt(selectedRow, 0);
        // Removing product
        enterprise.getProducts().getProducts().remove(product);
        if (product.getProductType().equals(ProductType.Gold)) {
            enterprise.getProducts().getGoldProducts().remove(product);
        } else {
            enterprise.getProducts().getSilverProducts().remove(product);
        }
        populateProductTable();
    }//GEN-LAST:event_btnRemoveProductActionPerformed

